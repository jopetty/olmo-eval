"""SciCode benchmark: scientific multi-step code generation.

Reference: https://github.com/scicode-bench/SciCode (dataset SciCode1/SciCode).

SciCode asks a model to implement the Python functions required to solve
multi-step scientific research problems. Each main problem decomposes into a
sequence of sub-steps with numeric reference answers stored in
``test_data.h5``. Two reported scores:

- **Sub-step accuracy**: fraction of sub-steps whose tests pass.
- **Main-problem accuracy**: fraction of problems where every sub-step passes.

Implementation notes
--------------------
Each main problem is emitted as a single ``Instance``. The model is prompted
with the full problem description, every sub-step description, every sub-step
function signature and return line, and the shared dependency imports. The
model returns one code block that must contain all of the sub-step functions.
The scorer then runs each sub-step's tests independently, records per-step
pass/fail in ``output.metadata['step_results']``, and the two metrics read
those results.

This design keeps the task inside the one-generation-per-instance abstraction
used by every other olmo-eval task, trading the upstream cascading-generation
prompt flow for reproducibility inside the harness.

Three upstream sub-steps (13.6, 62.1, 76.3) ship with hardcoded gold snippets
in the SciCode repo rather than being generated by the model. Those sub-steps
are marked as "provided" in the prompt (shown to the model as existing code)
and are injected verbatim into every test script so that later sub-steps
which depend on them can run. They are not scored — matching the upstream
denominator of 288 test / 50 validation sub-steps.

The scorer requires a sandbox that has ``numpy``, ``scipy``, ``sympy``, and
``h5py`` available, plus the SciCode numeric reference file at the path
configured via ``SciCodeExecutionScorer.h5py_file``. The scorer injects a
self-contained copy of the upstream ``process_hdf5_to_tuple`` helper into the
test script so the SciCode package does not need to be installed.
"""

