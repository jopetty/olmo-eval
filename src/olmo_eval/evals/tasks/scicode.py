# Inlined from scicode/parse/parse.py so the sandbox does not need the
# upstream `scicode` package installed to run the test scripts.
_H5_HELPER = """
import h5py as _h5py
import numpy as _np
import scipy as _scipy

def _scicode_process_hdf5_list(group):
    return [group[key][()] for key in group.keys()]

def _scicode_process_hdf5_sparse_matrix(group):
    data = group["data"][()]
    shape = tuple(group["shape"][()])
    if "row" in group and "col" in group:
        row = group["row"][()]
        col = group["col"][()]
        return _scipy.sparse.coo_matrix((data, (row, col)), shape=shape)
    if "blocksize" in group:
        indices = group["indices"][()]
        indptr = group["indptr"][()]
        blocksize = tuple(group["blocksize"][()])
        return _scipy.sparse.bsr_matrix(
            (data, indices, indptr), shape=shape, blocksize=blocksize
        )
    indices = group["indices"][()]
    indptr = group["indptr"][()]
    return _scipy.sparse.csr_matrix((data, indices, indptr), shape=shape)

def _scicode_process_hdf5_dict(group):
    out = {}
    for key, obj in group.items():
        if isinstance(obj, _h5py.Group):
            out[key] = _scicode_process_hdf5_sparse_matrix(obj["sparse_matrix"])
        elif isinstance(obj[()], bytes):
            out[key] = obj[()].decode("utf-8", errors="strict")
        else:
            try:
                out[float(key)] = obj[()]
            except ValueError:
                out[key] = obj[()]
    return out

def _scicode_process_hdf5_datagroup(group):
    for key in group.keys():
        if key == "list":
            return _scicode_process_hdf5_list(group[key])
        if key == "sparse_matrix":
            return _scicode_process_hdf5_sparse_matrix(group[key])
        return _scicode_process_hdf5_dict(group)

def process_hdf5_to_tuple(step_id, test_num, h5py_file):
    data_lst = []
    with _h5py.File(h5py_file, "r") as f:
        for test_id in range(test_num):
            group_path = f"{step_id}/test{test_id + 1}"
            node = f[group_path]
            if not isinstance(node, _h5py.Group):
                raise FileNotFoundError(f"Path {group_path} not found in the file.")
            keys = list(node.keys())
            if len(keys) == 1:
                sub = node[keys[0]]
                if isinstance(sub, _h5py.Dataset):
                    val = sub[()]
                    data_lst.append(val.decode("utf-8") if isinstance(val, bytes) else val)
                else:
                    data_lst.append(_scicode_process_hdf5_datagroup(sub))
            else:
                var_lst = []
                for key in node.keys():
                    sub = node[key]
                    if isinstance(sub, _h5py.Dataset):
                        val = sub[()]
                        var_lst.append(val.decode("utf-8") if isinstance(val, bytes) else val)
                    else:
                        var_lst.append(_scicode_process_hdf5_datagroup(sub))
                data_lst.append(tuple(var_lst))
    return data_lst
"""


_DEFAULT_PROMPT_TEMPLATE = """\
PROBLEM DESCRIPTION:
You will be provided with a multi-step scientific problem. Your task is to implement \
every sub-step by writing the requested Python functions.

MAIN PROBLEM:
{problem_description_main}

SUB-STEPS:
{steps_block}

DEPENDENCIES:
Use only the following dependencies. Do not include these imports in your answer.
{dependencies}

RESPONSE GUIDELINES:
1. Reply with a single ```python``` code block that contains every sub-step function.
2. Use exactly the function signatures provided for each sub-step.
3. Later sub-step functions may call earlier ones; define functions in order.
4. Do not include example usage, tests, or re-import the provided dependencies.
"""


_WITH_BACKGROUND_PROMPT_TEMPLATE = """\
PROBLEM DESCRIPTION:
You will be provided with a multi-step scientific problem along with the domain \
background required for each sub-step. Implement every sub-step.

MAIN PROBLEM:
{problem_description_main}

{problem_background_main}

SUB-STEPS:
{steps_block}

DEPENDENCIES:
Use only the following dependencies. Do not include these imports in your answer.
{dependencies}

RESPONSE GUIDELINES:
1. Reply with a single ```python``` code block that contains every sub-step function.
2. Use exactly the function signatures provided for each sub-step.
3. Later sub-step functions may call earlier ones; define functions in order.
4. Do not include example usage, tests, or re-import the provided dependencies.
"""


def _format_step(
    step: dict[str, Any],
    with_background: bool,
    hardcoded_code: str | None = None,
) -> str:
    if hardcoded_code is not None:
        return (
            f"### Step {step['step_number']} (pre-provided; use as-is, do not re-implement)\n\n"
            f"```python\n{hardcoded_code.rstrip()}\n```"
        )
    parts = [f"### Step {step['step_number']}", step["step_description_prompt"].strip()]
    if with_background and step.get("step_background"):
        parts.append(step["step_background"].strip())
    parts.append("Function signature:")
    parts.append(step["function_header"].rstrip())
    return_line = step.get("return_line")
    if return_line:
        parts.append(return_line.rstrip())
    return "\n\n".join(parts)


def _build_prompt(doc: dict[str, Any], with_background: bool) -> str:
    template = _WITH_BACKGROUND_PROMPT_TEMPLATE if with_background else _DEFAULT_PROMPT_TEMPLATE
    problem_id = str(doc["problem_id"])
    hardcoded = _HARDCODED_SNIPPETS.get(problem_id, {})
    steps_block = "\n\n".join(
        _format_step(step, with_background=with_background, hardcoded_code=hardcoded.get(idx))
        for idx, step in enumerate(doc["sub_steps"])
    )
    return template.format(
        problem_description_main=doc["problem_description_main"].strip(),
        problem_background_main=(doc.get("problem_background_main") or "").strip(),
        steps_block=steps_block,
        dependencies=doc["required_dependencies"].strip(),
    )


def _strip_import_lines(code: str) -> str:
    kept = []
    for line in code.split("\n"):
        stripped = line.lstrip()
        if stripped.startswith("import ") or stripped.startswith("from "):
            continue
        kept.append(line)
    return "\n".join(kept)


@dataclass(frozen=True, slots=True)
class SciCodeExecutionScorer(ExecutionScorer):
    """Run each sub-step's tests against the model's generated code."""

    name: str = "scicode_exec"
    timeout: float = 600.0
    h5py_file: str = DEFAULT_H5PY_PATH

    async def ascore(
        self,
        instance: Instance,
        output: LMOutput,
        execution_env: ExecutionEnvironment,
    ) -> float:
        code = output.extracted_answer
        sub_steps = instance.metadata.get("sub_steps", [])
        deps = instance.metadata.get("required_dependencies", "")
        hardcoded_prelude = instance.metadata.get("hardcoded_prelude", "")
        scorable_steps = [s for s in sub_steps if not s.get("_hardcoded")]
        if not code or not scorable_steps:
            output.metadata["step_results"] = [False] * len(scorable_steps)
            output.metadata["passed_steps"] = 0
            output.metadata["total_steps"] = len(scorable_steps)
            return 0.0

        results: list[bool] = []
        for step in scorable_steps:
            script = self._build_step_script(step, deps, code, hardcoded_prelude)
            exec_result = await execution_env.execute_code(
                script, language="python", timeout=self.timeout
            )
            results.append(bool(exec_result.success))

        output.metadata["step_results"] = results
        output.metadata["passed_steps"] = sum(results)
        output.metadata["total_steps"] = len(scorable_steps)
        return float(sum(results)) / len(scorable_steps) if scorable_steps else 0.0

    def _build_step_script(
        self,
        step: dict[str, Any],
        deps: str,
        code: str,
        hardcoded_prelude: str = "",
    ) -> str:
        step_id = step["step_number"]
        test_cases = list(step.get("test_cases") or ())
        lines: list[str] = [deps.strip(), _H5_HELPER.strip()]
        if hardcoded_prelude.strip():
            lines.append(hardcoded_prelude.strip())
        lines.append(code.strip())
        lines.append(
            f"targets = process_hdf5_to_tuple({step_id!r}, {len(test_cases)}, {self.h5py_file!r})"
        )
        for idx, tc in enumerate(test_cases):
            lines.append(f"target = targets[{idx}]")
            lines.append(tc)
        return "\n\n".join(lines) + "\n"


@dataclass(frozen=True, slots=True)
class SciCodeSubStepAccuracyMetric(Metric):
    """Micro-averaged sub-step accuracy (passed / total across all sub-steps)."""

    name: str = "sub_step_accuracy"
    scorer: type[Scorer] = SciCodeExecutionScorer

    def compute(self, responses: Sequence[Response]) -> float:
        total = 0
        passed = 0
        for response in responses:
            for output in response.outputs:
                meta = output.metadata or {}
                total += int(meta.get("total_steps", 0))
                passed += int(meta.get("passed_steps", 0))
        return passed / total if total else 0.0


@dataclass(frozen=True, slots=True)
class SciCodeMainProblemAccuracyMetric(Metric):
    """Fraction of main problems where every sub-step passes."""

    name: str = "main_problem_accuracy"
    scorer: type[Scorer] = SciCodeExecutionScorer

    def compute(self, responses: Sequence[Response]) -> float:
        if not responses:
            return 0.0
        correct = 0
        for response in responses:
            if not response.outputs:
                continue
            output = response.outputs[0]
            meta = output.metadata or {}
            total = int(meta.get("total_steps", 0))
            passed = int(meta.get("passed_steps", 0))
            if total > 0 and passed == total:
                correct += 1
        return correct / len(responses)


@register("scicode")
class SciCode(Task):
    """SciCode multi-step scientific code generation task."""

    data_source = DataSource(path="SciCode1/SciCode", split="test")
    sampling_params = SamplingParams(max_tokens=16384, temperature=0.0)
    metrics = (
        SciCodeSubStepAccuracyMetric(),
        SciCodeMainProblemAccuracyMetric(),
    )
    primary_metric = SciCodeSubStepAccuracyMetric()
    with_background: bool = False

    @property
    def instances(self) -> Iterator[Instance]:
        if self._instances_cache is None:
            self._instances_cache = []
            loader = DataLoader()
            source = self.config.get_data_source()
            for doc in loader.load(source):
                instance = self.process_doc(doc)
                if instance is not None:
                    self._instances_cache.append(instance)
        yield from self._instances_cache

    def process_doc(self, doc: dict[str, Any], index: int = 0) -> Instance | None:
        problem_id = str(doc["problem_id"])
        sub_steps = [dict(s) for s in doc["sub_steps"]]
        if not sub_steps:
            return None

        hardcoded = _HARDCODED_SNIPPETS.get(problem_id, {})
        hardcoded_prelude_parts: list[str] = []
        for idx, step in enumerate(sub_steps):
            if idx in hardcoded:
                step["_hardcoded"] = True
                hardcoded_prelude_parts.append(hardcoded[idx])
        scorable_count = sum(1 for s in sub_steps if not s.get("_hardcoded"))

        with_background = getattr(self, "with_background", False)
        prompt = _build_prompt(doc, with_background=with_background)
        return Instance(
            question=prompt,
            gold_answer=doc.get("general_solution"),
            metadata={
                "id": problem_id,
                "problem_id": problem_id,
                "problem_name": doc.get("problem_name"),
                "required_dependencies": doc["required_dependencies"],
                "sub_steps": sub_steps,
                "total_steps": scorable_count,
                "hardcoded_prelude": "\n\n".join(hardcoded_prelude_parts),
            },
        )

    def _build_fewshot(self) -> list[Instance]:
        return []

    def format_request(self, instance: Instance) -> LMRequest:
        if self.config.formatter is not None:
            return self.config.formatter.format(instance, [])
        return LMRequest(request_type=self.request_type, prompt=instance.question)

    def extract_answer(self, output: LMOutput) -> str | None:
        code = extract_code(output.text)
        if not code:
            return None
        return _strip_import_lines(code)


